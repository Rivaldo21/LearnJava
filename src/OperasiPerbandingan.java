public class OperasiPerbandingan {
    public static void main(String[] args) {

        int a = 100;
        int b = 100;

        System.out.println(a > b); // Nilai "a" lebih dari "b"?
        System.out.println(a < b); // Nilai "a" kurang dari "b"?
        System.out.println(a >= b); // Nilai "a" lebih dari sama dengan "b"?
        System.out.println(a <= b); // Nilai "a" kurang dari sama dengan "b"?
        System.out.println(a == b); // Nilai "a" Sama dengan "b"?
        System.out.println(a != b); // Nilai "a" Tidak sama dengan "b"?
    }
}
